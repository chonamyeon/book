const updatedCelebrities = celebrities.map(celeb => {
    const updatedBooks = celeb.books.map(book => {
        let cleanReview = book.review || "";

        // Clean up previous references
        cleanReview = cleanReview.replace(/^\[ 참고도서 : .*\n+/gm, "");
        cleanReview = cleanReview.replace(/\n+\[ 참고도서 : .*\]$/gm, "");

        // 1. Remove ### headers
        cleanReview = cleanReview.replace(/^###\s+.*\n+/gm, "");
        // 2. Reduce multiple newlines
        cleanReview = cleanReview.replace(/\n{3,}/g, "\n\n");

        // 3. Replace repetitive "지식의 아카이브" with varied phrases
        const alternatives = ["아카이뷰의 서가", "지혜의 보고", "이곳의 기록물", "우리의 서고", "엄선된 지식", "사유의 기록"];
        let altIdx = 0;
        cleanReview = cleanReview.replace(/지식의 아카이브/g, () => {
            const pick = alternatives[altIdx % alternatives.length];
            altIdx++;
            return pick;
        });

        // 4. Trim
        cleanReview = cleanReview.split('\n').map(line => line.trim()).join('\n');

        const pub = publishers[book.id] || "김영사";
        const sourceLine = `\n\n[ 참고도서 : ${book.title}, 저자 : ${book.author}, 출판사 : ${pub} ]`;

        const info = insights[book.id] || { summary: "지혜를 갈무리하는 중입니다." };

        // Combine all metadata into a single insight string for the final page
        const detailedInsight = `✨ 책을 선택한 이유\n${info.reason || "공개 예정"}\n\n🖋️ 저자 소개\n${info.author || "공개 예정"}\n\n🙌 추천 대상\n${info.target || "공개 예정"}\n\n💡 지혜의 요약\n${info.summary}`;

        return {
            ...book,
            review: cleanReview + sourceLine,
            insight: detailedInsight
        };
    });
    return { ...celeb, books: updatedBooks };
});

